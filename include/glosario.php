<section>
	<header class="main">
		<h1 class="titulo">Gosario de términos</h1>
    </header>
    
	<ul style="margin-left: 2em">
		<li><strong>Alta dirección:</strong> Persona o grupo de personas que dirige y controla una al más alto nivel. La alta dirección tiene el poder para delegar autoridad y proporcionar recursos dentro de la organización.</li>
		<li><strong>Autoridad:</strong> Facultad o derecho de mandar.</li>
		<li><strong>Cliente:</strong> Persona u organización que podría recibir o que recibe un producto o un servicio destinado a esa persona u organización o requerido por ella.</li>
		<li><strong>Documento:</strong>> Información y el medio en el que está contenida.</li>
		<li><strong>Gestión:</strong> Conjunto de operaciones que se realizan para dirigir y administrar un negocio o una empresa.</li>
		<li><strong>Gestión documental:</strong> La gestión documental es el conjunto de procesos que se enfocan en la revisión, almacenamiento y recuperación de los documentos y de la información de importancia que se manejan al interior de una empresa. Esto se lleva a cabo por medio de la aplicación de normas técnicas que se caracterizan por su practicidad al momento de llevar a cabo la administración de los archivos físicos y electrónicos.</li>
		<li><strong>SIG:</strong> Sistema Integrado de Gestión.</li>
	</ul>


